from math import floor, ceil


class Pattern:
    """Controls movement of pattern."""

    def __init__(self, grid):
        self.grid = grid

    def flip_vertical(self):
        """Vertical reflection."""
        return Pattern(self.grid[::-1, :])

    def flip_horizontal(self):
        """Horizontal reflection."""
        return Pattern(self.grid[:, ::-1])

    def flip_diag(self):
        """Diagonal reflection."""
        return Pattern(self.grid.T)

    def rotate(self, n):
        """Rotation anti-clockwise in right-angle multiples."""
        out = self
        for i in range(n):
            out = out.flip_diag().flip_vertical()
        return out


