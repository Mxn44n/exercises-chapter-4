class Game:
    """A game of Conway's Life with a square board of side length size."""

    def __init__(self, size):
        self.board = np.zeros((size, size))

    def play(self):
        """Play the game indefinitely until interrupted."""
        print("Playing life. Press ctrl + c to stop.")
        pyplot.ion()
        while True:
            self.move()
            self.show()
            pyplot.pause(0.0000005)

    def move(self):
        """Play one step of the game."""
        stencil = np.array([[1, 1, 1], [1, 0, 1], [1, 1, 1]])
        neighbourcount = convolve2d(self.board, stencil, mode='same')

        for i in range(self.board.shape[0]):
            for j in range(self.board.shape[1]):
                self.board[i, j] = 1 if (neighbourcount[i, j] == 3 or (
                    neighbourcount[i, j] == 2 and self.board[i, j])) else 0

    def __setitem__(self, key, value):
        """Insert a pattern centred on the location given."""
        self.board[key] = value

    def show(self):
        """Visualise the current game board."""
        pyplot.clf()
        pyplot.matshow(self.board, fignum=0, cmap='binary')
        pyplot.show()

    def insert(self, pattern, location):
        """Put pattern on map."""
        rows, cols = pattern.grid.shape
        x, y = location
        self[x - floor(rows/2):x + ceil(rows/2),
             y - floor(cols/2):y + ceil(cols/2)] = pattern.grid
